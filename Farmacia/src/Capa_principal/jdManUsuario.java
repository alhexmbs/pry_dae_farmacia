    private void txtCelularUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCelularUsuarioKeyTyped
        // TODO add your handling code here:
        int x = evt.getKeyChar();
        boolean validacion = x >= 48 && x <= 57;
        if (!validacion) {
            evt.consume();
        }

        if (txtCelularUsuario.getText().length() >= 9) {
            evt.consume();
        }
    }//GEN-LAST:event_txtCelularUsuarioKeyTyped

    private void txtSueldoUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSueldoUsuarioKeyTyped
        // TODO add your handling code here:
        int x = evt.getKeyChar();
        boolean validacion = x >= 48 && x <= 57 || x == 46;
        if (!validacion) {
            evt.consume();
        }

        if (txtSueldoUsuario.getText().length() >= 13) {
            evt.consume();
        }
    }//GEN-LAST:event_txtSueldoUsuarioKeyTyped

