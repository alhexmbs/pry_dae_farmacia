    private int definirLimite(){
        String tipoDoc = cboTipoDoc.getSelectedItem().toString();
        int limite = 0;
        
        switch (tipoDoc) {
            case "DNI":
                limite = 8;
                break;
            case "Carné de Extranjería":
                limite = 12;
                break;
            case "Pasaporte":
                limite = 15;
                break;
        }
        
        return limite;
    }
    
    private void txtNumDocUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumDocUsuarioKeyTyped
        // TODO add your handling code here:
        int limite = definirLimite();

        int x = evt.getKeyChar();
        boolean validacion = x >= 48 && x <= 57;
        if(!validacion){
            evt.consume();
        }

        if(txtNumDocUsuario.getText().length() >= limite){
            evt.consume();
        }
    }//GEN-LAST:event_txtNumDocUsuarioKeyTyped

