    private int definirLimite() {
        String tipoDoc = cboTipoDoc.getSelectedItem().toString();
        int limite = 0;

        switch (tipoDoc) {
            case "DNI":
                limite = 8;
                break;
            case "Carné de Extranjería":
                limite = 12;
                break;
            case "Pasaporte":
                limite = 15;
                break;
        }

        return limite;
    }

